enum SettingsLayout {
    /// Spacing
    static let defaultSpacing: CGFloat = 12
    static let spacingBetweenUserInformation: CGFloat = 24
    static let spacingBetweenContent: CGFloat = 48
}

// MARK: - User Information Constants
extension SettingsLayout {
    /// Padding
    static let topPadding: CGFloat = 36
    /// Avatar image
    static let defaultAvatarSize: CGFloat = 72
}

// MARK: - Settings Cell Constants
extension SettingsLayout {
    /// Icon
    static let cornerRadius: CGFloat = 8
    static let bgIconSize: CGFloat = 40
    static let iconWidth: CGFloat = 16
    static let iconHeight: CGFloat = 20
}
